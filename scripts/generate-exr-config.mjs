#!/usr/bin/env node
/**
 * Scans env/exr/*_env.exr and writes src/exrEnvironments.generated.js
 */
import { readdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const exrDir = join(root, "env/exr");
const outFile = join(root, "src/exrEnvironments.generated.js");

function toId(stem) {
  const id = stem
    .replace(/\.(jpg|jpeg|heic)$/i, "")
    .replace(/[^a-zA-Z0-9]+/g, "_")
    .replace(/^_|_$/g, "")
    .toLowerCase();
  return /^\d+$/.test(id) ? `exr_${id}` : id;
}

function toLabel(stem) {
  return stem.replace(/\.(jpg|jpeg|heic)$/i, "");
}

const files = readdirSync(exrDir)
  .filter((f) => f.endsWith("_env.exr"))
  .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

const entries = files.map((file) => {
  const stem = file.replace(/_env\.exr$/i, "");
  const id = toId(stem);
  return [id, { label: toLabel(stem), file }];
});

const body = entries
  .map(([id, { label, file }]) => `  ${id}: {\n    label: ${JSON.stringify(label)},\n    file: ${JSON.stringify(file)},\n  },`)
  .join("\n");

const source = `/** Auto-generated by scripts/generate-exr-config.mjs — do not edit. */
export const EXR_ENVIRONMENTS = {
${body}
};
`;

writeFileSync(outFile, source);
console.log(`Wrote ${entries.length} environments → ${outFile}`);
